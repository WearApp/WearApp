- (UIColor *)colorWithNumString:(NSString *)number {
    NSArray *temp = self.dictionary[number];
    NSNumber *red = temp[0];
    NSNumber *green = temp[1];
    NSNumber *blue = temp[2];
    self.color = [UIColor colorWithRed:red.floatValue/255.0f green:green.floatValue/255.0f blue:blue.floatValue/255.0f alpha:0.9];
    
//    NSLog(@"%@",self.color.description);
    return self.color;
}

